
public class Exemplo1 {
    public static void main(String[] args) {

        // Vamos utilizar o metodo (for), para incrementação;
        // Vamos contar até 20 cordeiros;
        for (int cordeiro = 0; cordeiro <= 20; cordeiro++) {
            System.out.println("Quantidade de cordeiro :" + cordeiro);
        }

    }
}
